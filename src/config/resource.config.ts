/**
 * Defines standard aspect ratios commonly used for media, particularly images.
 *
 * The enum values represent the ratio as strings (e.g., '1x1', '16x9').
 * These values are often used as keys in {@link FocusMap} or for requesting
 * specific renditions of media assets.
 */
export enum MediaImageRatio {
  oneXone = '1x1',
  twoXone = '2x1',
  fourXseven = '4x7',
  sixteenXnine = '16x9',
}
